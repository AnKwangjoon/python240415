# 리스트 (List)
my_list = [1, 2, 3, 4, 5]
print("List:", my_list)
print("Length:", len(my_list))  # 길이 확인
print("First Element:", my_list[0])  # 첫 번째 요소 접근

# 튜플 (Tuple)
my_tuple = (1, 2, 3, 4, 5)
print("\nTuple:", my_tuple)
print("Length:", len(my_tuple))  # 길이 확인
print("First Element:", my_tuple[0])  # 첫 번째 요소 접근

# 세트 (Set)
my_set = {1, 2, 3, 4, 5}
print("\nSet:", my_set)
print("Length:", len(my_set))  # 길이 확인

# 딕셔너리 (Dictionary)
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}
print("\nDictionary:", my_dict)
print("Length:", len(my_dict))  # 길이 확인
print("Value for 'a':", my_dict['a'])  # 'a' 키에 해당하는 값 접근
